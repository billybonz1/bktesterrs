<H5><BR>Ставки на номера (Inside bets)</H5>
<P><B><A name=a></A>Ставка на один номер (Straight Up)</B></P>
<P>Фишка ставится на поле с обозначением выбранного номера, 
возможна также ставка на 0 (Zero) и на 00 (Double Zero). 
Существует 38 различных вариантов ставки. Ставка выигрывает 
если шарик падает на выбранное число.<BR>Пример: <B><A title="How to place bet" href="#example">A</A></B> 
(30) Шансы: <B>38:1</B> Выплата: <B>35:1</B></P>
<P><B><A name=b></A>Ставка на два номера (Split Bet)</B></P>
<P>Фишка ставится на черту, разделяющую любые два числа, т.е. 
возможна ставка и на 0 и 00, и на 0 в комбинации с любым 
числом первого ряда. Существует 57 различных вариантов ставки. 
Ставка выигрывает если шарик падает на любое из выбранных 
чисел.<BR>Пример: <B><A title="How to place bet" href="#example">B</A></B> 
(11 или 14). Шансы: <B>38:2</B> Выплата: <B>17:1</B></P>
<P><B><A name=c></A>СТРИТ или ставка на три числа (Street 
Bet)</B></P>
<P>Фишка ставится на линию, которая отделяет нумерованное поле 
от внешней области и покрывает все три числа в выбранном ряду. 
Существует 12 различных вариантов ставки. Ставка выигрывает 
если шарик падает на любое из трех чисел выбранного 
ряда.<BR>Пример: <B><A title="How to place bet" href="#example">C</A></B> 
(19, 20, 21). Шансы: <B>38:3</B> Выплата: <B>11:1</B></P>
<P><B><A name=d></A>Ставка на четыре номера (Corner 
Bet)</B></P>
<P>Фишка ставится на точку соприкосновения четырех чисел. 
Существует 22 различных варианта ставки. Ставка выигрывает 
если шарик падает на любое из четырех чисел.<BR>Пример: <B><A 
title="How to place bet" 
href="#example">D</A> 
</B>(25, 26, 28, 29). Шансы: <B>38:4</B> Выплата: 
<B>8:1</B></P>
<P><B><A name=f></A>Ставка на два ряда - шесть номеров (Line 
Bet</B>)</P>
<P>Фишка ставится на внешнюю линию в месте соприкосновения 
двух рядов. Существует 11 различных вариантов ставки, которая 
выигрывает если шарик падает на любой из шести номеров, 
принадлежащих этим двум рядам. <BR>Пример: <B><A title="How to place bet" href="#example">F</A></B> 
(4, 5, 6, 7, 8, 9). Шансы: <B>38:6</B> Выплата: <B>5:1</B></P>
<H5>Внешние ставки (Outside bets)</H5>
<P><B><A title="How to place bet" name=g></A>Ставка на колонку 
или столбец (Column Bet</B>)</P>
<P>В каждой колонке на игровом поле 12 номеров. Фишка ставится 
на поле, помеченное "2 to 1" и покрывает все номера в этом 
столбце (но не "0" и "00"!). Существует 3 варианта 
ставки.<BR>Пример: <B><A title="How to place bet" href="#example">G</A></B> 
(Все числа в колонке). Шансы: <B>38:12</B> Выплата: 
<B>2:1</B></P>
<P><B><A name=h></A>Ставка на дюжину (Dozen Bet)</B></P>
<P>Фишка ставится на поле с обозначением выбранной дюжины. Как 
и для Column Bet, возможны 3 варианта ставок.<BR>Пример: <B><A title="How to place bet" href="#example">H</A></B> 
(Третья дюжина - номера от 25 по 36). Шансы: <B>38:12</B> 
Выплата: <B>2:1</B></P>
<P><B><A name=i></A>Красное или Черное (Red or 
Black</B><B>)</B></P>
<P>Фишка ставится на "RED" или "BLACK" (красное или черное) и 
покрывает соответственно все красные, либо черные номера. Zero 
и Double Zero цвета не имеют, и если выпадает 0 или 00 вы 
теряете свою ставку.<BR>Пример: <B><A title="How to place bet" href="#example">I</A></B> 
(Красное). Шансы: <B>38:18</B> Выплата: <B>1:1</B></P>
<P><B><A name=j></A>Четное или Нечетное (Even or Odd</B><B>)</B></P>
<P>Аналогично "RED/BLACK", но ставка делается либо на четные, 
либо нечетные номера. Как и предыдущая, Zero и Double Zero 
этой ставкой не покрываются. <BR>Пример: <B><A title="How to place bet" href="#example">J</A> 
</B>(Odd numbers). Шансы: <B>38:18</B> Выплата: <B>1:1</B></P>
<P><B><A name=k></A>Маленькое или Большое (Low or 
High</B><B>)</B></P>
<P>Игровое поле разделяется на две группы: номера от 1 до 18 
(малые) и от 19 до 36 (большие). Вы делаете ставку на 
соответствующее поле, пытаясь угадать, к какой группе будет 
принадлежать следующее число. Если выпадает 0 или 00 вы 
теряете свою ставку. Пример: <B><A title="How to place bet" href="#example">K</A></B> 
(Большое - от 19 по 36). Шансы: <B>38:18</B> Выплата: 
<B>1:1</B></P>
