<H4>Цель игры</H4>
<P>Цель игры - угадать, на какое число из 37 упадет шарик.</P>
<H4>Колесо в рулетке</H4>
<TABLE width="100%" border=0>
<TBODY>
<TR>
<TD>
<P>На колесе рулетки тридцать семь чисел, от 1 до 36 и 0 
(ZERO). Иногда эту игру называют "рулеткой с одним 
нулем". </P>
<P>Колесо в рулетке имеет 37 нумерованных отделений, от 
0 до 36.</P>
<UL>
<LI>Красные и черные числа чередуются. 
<LI>В секторе, расположенном справа от ноля (числа от 
32 до 10) девять черных малых чисел (2, 4, 6, 8, 10, 
11, 13, 15, 17) и девять красных больших чисел (19, 
21, 23, 25, 27, 30, 32, 34, 36). 
<LI>В секторе слева от ноля (от 26 до 5) - девять 
черных больших (20, 22, 24, 26, 28, 29, 31, 33, 35) и 
девять красных малых чисел (1, 3, 5, 7, 9, 12, 14, 16, 
18). </LI></UL>
<P>Числа нанесены по внешней окружности колеса.</P></TD>
</TR></TBODY></TABLE>
<H4>Игровой стол в рулетке</H4>
<P><IMG alt="Игровое поле" src="img/layout.gif" width=150 align=right> 
Рулетка играется за столом, на котором находится колесо, и 
игровое поле с нанесенными на нем числами (номерами). </P>
<P>На стандартном поле для рулетки выделяют:</P>
<UL>
<LI>Внутренне поле (Inner section) 
<LI>Внешнее поле (Otside section) </LI></UL>
<H5>Внутренне поле</H5>
<P>Внутренне поле содержит все номера, которые нанесены на 
колесе. Каждые три последовательные числа образуют ряд (row), 
вертикальные линии - три колонки (column) по двенадцать чисел 
в каждой.</P>
<H5>Внешнее поле</H5>
<P>Специальных полях по краям игрового поля для внешних 
ставок. Каждое такое поле соответствует либо определенным 
числам (например - первая дюжина), либо каким-то 
характеристикам чисел (например - красные номера). </P>
<P>&nbsp;</P>
<P>Игрок может делать ставки на: </P>
<UL>
<LI>Определенное число 
<LI>Комбинацию чисел 
<LI>Будет ли число красным или черным, четным или нечетным, 
большим или малым </LI></UL>
<H4 class=pagetitle>Ставки в рулетке</H4>
<P>В рулетке возможны 10 различных типов ставок, каждая 
покрывает определенный диапазон номеров (либо один номер) и в 
случае выигрыша оплачивается по разному.</P>
<H5>Игровое поле в рулетке</H5>
<P>На игровом поле нанесены номера от 0 до 36, которые 
образуют ряды (rows) по три числа в каждом и колонки (columns) 
по 12 чисел в каждом. </P>
<P>Первые шесть типов ставок делаются в пределах границ 
игрового поля и называются ставки на номера (или внутренние 
ставки - inside bets). Остальные ставки делаются на 
специальных полях по краям игрового поля (внешние ставки - 
outside bets)</P>
